
public class BlobProperties {
	
	private String blobUrl;
	private String blobName;
	private String serverFrom;
	private String serverTo;
	private String container;
	/**
	 * @return the blobUrl
	 */
	public String getBlobUrl() {
		return blobUrl;
	}
	/**
	 * @param blobUrl the blobUrl to set
	 */
	public void setBlobUrl(String blobUrl) {
		this.blobUrl = blobUrl;
	}
	/**
	 * @return the blobName
	 */
	public String getBlobName() {
		return blobName;
	}
	/**
	 * @param blobName the blobName to set
	 */
	public void setBlobName(String blobName) {
		this.blobName = blobName;
	}
	/**
	 * @return the serverFrom
	 */
	public String getServerFrom() {
		return serverFrom;
	}
	/**
	 * @param serverFrom the serverFrom to set
	 */
	public void setServerFrom(String serverFrom) {
		this.serverFrom = serverFrom;
	}
	/**
	 * @return the serverTo
	 */
	public String getServerTo() {
		return serverTo;
	}
	/**
	 * @param serverTo the serverTo to set
	 */
	public void setServerTo(String serverTo) {
		this.serverTo = serverTo;
	}
	/**
	 * @return the container
	 */
	public String getContainer() {
		return container;
	}
	/**
	 * @param container the container to set
	 */
	public void setContainer(String container) {
		this.container = container;
	}
	

}
